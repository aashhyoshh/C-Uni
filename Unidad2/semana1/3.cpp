#include <iostream>
using namespace std;

int main (){

    int i=1, n, suma = 0;
    cout << "Ingrese un numero entero positivo: ";
    cin >> n;

    while ( i <= n ) {
        suma = suma + i;
        i++;
    }
    cout << "la suma es:" << suma << endl;
}





